#!/bin/ksh

rm -f kd.old

if [ -f kd.clog ]
then
    mv kd.clog kd.old
fi

./knowndata 10 | ./blog -l 100K -D knowndata kd.clog

echo "------------------------------------------------------------------------------"
./blog -C kd.clog
echo "------------------------------------------------------------------------------"
./blog -d kd.clog | head -3
echo "------------------------------------------------------------------------------"
